package lesson3_hw;

public class Calculator {

    public int summ (int oneValue, int twoValue){
         return oneValue + twoValue;
    }

    public double summ (double oneValue, double twoValue){
         return oneValue + twoValue;
    }

    public long summ (long oneValue, long twoValue){
         return oneValue + twoValue;
    }
    public double division (int oneValue, int twoValue){
         return oneValue / twoValue;
    }

    public double division (double oneValue, double twoValue){
         return oneValue / twoValue;
    }

    public double division (long oneValue, long twoValue){
         return oneValue / twoValue;
    }
    public int multiplication (int oneValue, int twoValue){
         return oneValue * twoValue;
    }

    public double multiplication (double oneValue, double twoValue){
         return oneValue * twoValue;
    }

    public long multiplication (long oneValue, long twoValue){
         return oneValue * twoValue;
    }
    public int subtraction (int oneValue, int twoValue){
         return oneValue - twoValue;
    }

    public double subtraction (double oneValue, double twoValue){
         return oneValue - twoValue;
    }

    public long subtraction (long oneValue, long twoValue){
         return oneValue - twoValue;
    }
}
